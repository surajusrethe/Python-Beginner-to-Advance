#Different String function

#function is a block of code which does specific task.
name = 'gango bai'

#to access specif element 
print(name[0])

#to convert String into uppercase
print(name.upper())

#to check String is Uppercase or Lowercase
print(name.islower())
print(name.upper().isupper())

#To get the length
print(len(name))

#to get the index of specific character
print(name.index('b'))

#to replace specific character
print(name.replace('b','t'))





